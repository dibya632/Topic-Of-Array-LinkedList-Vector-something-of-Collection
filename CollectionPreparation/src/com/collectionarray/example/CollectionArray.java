package com.collectionarray.example;

public class CollectionArray {
	public static void main(String[]args) {
		
	
		
		int[] a= {10,20,20,30,50,60,70,100};
		
		int sum=0;
		
		for(int i=0;i<a.length;i++)
		
		 {
			
			sum=sum+a[i];
			
		}
			
			System.out.println("Sum of the number is:"+sum);
			
			
		
		
			
		
	}

}
